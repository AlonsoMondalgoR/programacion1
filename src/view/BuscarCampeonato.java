    private void listaCampeonato(){
        List<Campeonato>data=model.listaCampeonato();
        DefaultTableModel dtm=(DefaultTableModel)table.getModel();
        dtm.setRowCount(0);
        for(Campeonato c:data){
            Object[]fila={ c.getIdCampeonato(),
                           c.getDescripcion(),
                           c.getAnho()};
            dtm.addRow(fila);
        }
        dtm.fireTableDataChanged();
        
    }
    
    
    
